export function gramsXkilogram(value: number) {

    return value * 0.001;
}

export function kilogramXgrams(value: number) {

    return value * 1000;
}

export function poundsXkilogram(value: number) {

    return value * 0.453592;
}

export function kilogramXpounds(value: number) {

    return value * 2.2046;
}

export function kilogramXtonne(value: number) {

    return value * 0.001;
}

export function tonneXkilogram(value: number) {

    return value * 1000;
}

export function poundsXtonne(value: number) {

    return value * 0.00045359;
}

export function tonneXpounds(value: number) {

    return value * 2204.62;
}
